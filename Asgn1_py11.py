"""Read 2 numbers to variable a and b and perform 
all bitwise operations on that numbers. """

a=int(input())
b=int(input())
print("Bitwise and operartor:",a&b)
print("Bitwise or operartor:",a|b)
print("Bitwise xor operartor:",a^b)
print("Bitwise one's complement operartor:",~a)
print("Bitwise one's complement operartor:",~b)
print("Bitwise leftshift operartor:",a<<b)
print("Bitwise rightshift operartor:",a>>b)
